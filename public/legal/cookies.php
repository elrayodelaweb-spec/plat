                <h3>Cookies Esenciales o Técnicas</h3>
                <p>Estas cookies son estrictamente necesarias para el funcionamiento básico de nuestra plataforma:</p>
                <ul>
                    <li><strong>Cookies de sesión:</strong> Mantienen su sesión de usuario activa mientras navega</li>
                    <li><strong>Cookies de seguridad:</strong> Protegen su cuenta contra accesos no autorizados</li>
                    <li><strong>Cookies de equilibrio de carga:</strong> Distribuyen el tráfico entre servidores</li>
                    <li><strong>Cookies de personalización:</strong> Recuerdan sus preferencias básicas</li>
                </ul>

                <h3>Cookies de Rendimiento y Análisis</h3>
                <p>Nos ayudan a comprender cómo los visitantes interactúan con nuestro sitio:</p>
                <ul>
                    <li><strong>Google Analytics:</strong> Analizan el tráfico y comportamiento de los usuarios</li>
                    <li><strong>Cookies de rendimiento:</strong> Miden el tiempo de carga y rendimiento del sitio</li>
                    <li><strong>Cookies de errores:</strong> Detectan y reportan problemas técnicos</li>
                    <li><strong>Cookies de pruebas A/B:</strong> Optimizan la experiencia del usuario</li>
                </ul>

                <h3>Cookies de Funcionalidad</h3>
                <p>Mejoran su experiencia recordando sus preferencias:</p>
                <ul>
                    <li><strong>Cookies de idioma:</strong> Recuerdan su idioma preferido</li>
                    <li><strong>Cookies de región:</strong> Muestran contenido relevante para su ubicación</li>
                    <li><strong>Cookies de personalización:</strong> Guardan sus configuraciones de visualización</li>
                    <li><strong>Cookies de preferencias:</strong> Recuerdan sus opciones de privacidad</li>
                </ul>
