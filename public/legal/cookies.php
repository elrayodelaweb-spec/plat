                    <li>Aceptar o rechazar todas las cookies</li>
                    <li>Ser notificado cuando se envía una cookie</li>
                    <li>Eliminar cookies existentes</li>
                    <li>Bloquear cookies de terceros</li>
                    <li>Navegar en modo privado/incógnito</li>
                </ul>

                <h3>Configuración por Navegador</h3>
