                <h2>1. ¿Qué son las Cookies?</h2>
                <p>Las cookies son pequeños archivos de texto que se almacenan en su dispositivo (computadora, tablet, teléfono móvil) cuando visita nuestro sitio web. Estas cookies nos ayudan a proporcionarle una experiencia de usuario mejorada y a entender cómo interactúa con nuestra plataforma.</p>
                <p>Las cookies son completamente seguras y no pueden ejecutar código ni contener virus. Su único propósito es identificar al usuario durante su visita a nuestro sitio web.</p>
            </div>

