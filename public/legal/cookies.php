                <h2>4. Duración de las Cookies</h2>
                <p>Las cookies pueden ser temporales o permanentes:</p>
                
                <h3>Cookies de Sesión</h3>
                <p>Se eliminan automáticamente cuando cierra su navegador. Se utilizan para mantener su sesión activa durante una única visita.</p>

                <h3>Cookies Persistentes</h3>
                <p>Permanecen en su dispositivo durante un período específico o hasta que las elimine manualmente. Estas cookies recuerdan sus preferencias en visitas futuras.</p>

                <h3>Plazos de Caducidad</h3>
                <ul>
                    <li><strong>Cortos (1-30 días):</strong> Cookies de análisis y rendimiento</li>
                    <li><strong>Medios (30-365 días):</strong> Cookies de funcionalidad</li>
                    <li><strong>Largos (más de 1 año):</strong> Cookies de preferencias de usuario</li>
                </ul>
            </div>

            <div class="section">
                <h2>5. Gestión de Cookies</h2>
                <p>Usted tiene control completo sobre las cookies. Puede configurar su navegador para:</p>
